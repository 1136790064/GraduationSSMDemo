package declare.entity;


public class Teacher {

  private String tno;
  private String name;
  private String password;
  private String isAdministrators;
  private String sex;
  private String college;
  private String title;
  private String subject;
  private String salaryNumber;
  private String phone;
  private String email;


  public String getTno() {
    return tno;
  }

  public void setTno(String tno) {
    this.tno = tno;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getPassword() {
    return password;
  }

  public void setPassword(String password) {
    this.password = password;
  }


  public String getIsAdministrators() {
    return isAdministrators;
  }

  public void setIsAdministrators(String isAdministrators) {
    this.isAdministrators = isAdministrators;
  }


  public String getSex() {
    return sex;
  }

  public void setSex(String sex) {
    this.sex = sex;
  }


  public String getCollege() {
    return college;
  }

  public void setCollege(String college) {
    this.college = college;
  }


  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }


  public String getSubject() {
    return subject;
  }

  public void setSubject(String subject) {
    this.subject = subject;
  }


  public String getSalaryNumber() {
    return salaryNumber;
  }

  public void setSalaryNumber(String salaryNumber) {
    this.salaryNumber = salaryNumber;
  }


  public String getPhone() {
    return phone;
  }

  public void setPhone(String phone) {
    this.phone = phone;
  }


  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }

}
